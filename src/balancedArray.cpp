void solve() {
    // Algorithm:
    // ==========
    // [2, 4, 6, 8, ..., n, 1, 3, 5, 7, ... (n-1)]
    // However, the right array sum is exactly (n/2) less than that of the left array
    // Thus, add (n/2) to the last element of the right array.
    //
    // Time: O(n)
    // Space: O(1)

    int t, n;
    cin >> t;
    while (t--) {
        cin >> n;

        // n % 4 != 0
        if ((n/2) & 1) {
            cout << "NO" << nl;
            continue;
        }

        cout << "YES" << nl;

        for (int i = 2; i <= n; i+=2)
            cout << i << " ";

        // add (n/2) to the right array
        for (int i = 1; i <= n; i+=2) {
            if (i == n - 1)
                cout << i + (n / 2) << nl;
            else
                cout << i << " ";
        }
    }
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    solve();

#ifdef _GLIBCXX_DEBUG
    cerr << endl << "finished in " << clock() * 1.0 / CLOCKS_PER_SEC << " sec" << endl;
#endif

    return 0;
}
