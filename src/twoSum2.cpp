vector<int> twoSum(vector<int>& numbers, int target) {
    int low = 0, high = numbers.size() - 1, currSum;
    vector<int> res;

    while (low <= high) {
        currSum = numbers[low] + numbers[high];

        if (currSum == target) {
            res.push_back(low + 1);
            res.push_back(high + 1);
            return res;
        }

        if (currSum < target)
            low++;
        else if (currSum > target)
            high--;
    }

    return res;
}

