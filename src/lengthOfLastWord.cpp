mt19937 mt_rand(chrono::high_resolution_clock::now().time_since_epoch().count());
const char nl = '\n';
const double PI = acos(-1);

int lengthOfLastWord(string s) {

    int n = s.length(), count = 0;

    for (int i = n - 1; i >= 0; i--) {
        if (count > 0 && s[i] == ' ')
            break;
        else if (s[i] != ' ')
            count++;
    }

    return count;
}

