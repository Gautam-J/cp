int climbStairs(int n) {
    // given n <= 45
    int dp[46];

    for (int i = 0; i <= 2; i++)
        dp[i] = i;

    for (int i = 3; i <= n; i++)
        dp[i] = dp[i - 1] + dp[i - 2];

    return dp[n];
}

