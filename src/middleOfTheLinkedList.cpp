// Definition for singly-linked list.
struct ListNode {
    int val;
    ListNode* next;
 };

ListNode* middleNode(ListNode* head) {
    ListNode* t1 = head;
    ListNode* t2 = head;

    while (t2 != NULL && t2->next != NULL) {
        t1 = t1->next;
        t2 = t2->next->next;
    }

    return t1;
}

