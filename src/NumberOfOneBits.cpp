int solve(uint32_t n) {
    if (n == 0)
        return 0;

    int cnt = 0;
    for (int i = 0; i < 32; i++) {
        if ((n & 1) == 1)
            cnt++;
        n >>= 1;  // dividing by 2
    }
    return cnt;
}

