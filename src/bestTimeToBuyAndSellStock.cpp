int solve(vi& a) {
    int n = a.size();
    int low = a[0];
    int profit = 0, varProfit = 0, high;

    for (int i = 1; i < n; i++) {
        if (low > a[i])
            low = a[i];
        else {
            high = a[i];
            varProfit = high - low;
        }
        profit = max(profit, varProfit);
    }
    return profit;
}

