bool solve(string& s) {
    string res;
    trav(i, s) {
        if (isalnum(i))
            res.push_back(i);
    }

    trav(i, res) {
        if (isupper(i))
            i = tolower(i);
    }

    int low = 0, high = res.size() - 1;
    while (low < high) {
        if (res.at(low) != res.at(high))
            return false;
        low++;
        high--;
    }

    return true;
}

