uint32_t solve(uint32_t n) {
    if (n == 0)
        return 0;

    int res = 0;
    for (int i = 0; i < 32; i++) {
        res <<= 1;  // multiply by 2
        if ((n & 1) == 1)
            res++;
        n >>= 1;  // divide by 2
    }
    return res;
}

