int solve(vi& a) {
    int low = 0, high = sz(a) - 1, mid;

    int res = sz(a);
    while (low <= high) {
        mid = low + (high - low) / 2;

        if (a[mid] == 1) {
            res = mid;
            high = mid - 1;
        } else if (a[mid] < 1) {
            low = mid + 1;
        } else {
            high = mid - 1;
        }
    }

    return sz(a) - res;
}

